def apply_pack():
    with open(DATA_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    applied, skipped = 0, 0
    for post in data:
        d = post.get("day")
        if d in PACK:
            new = PACK[d]
            # 정책: 상태 필드(approved/published/scheduled_date/scheduled_time)는 보존
            for field in ("book_title", "excerpt", "hook", "caption", "hashtags"):
                post[field] = new[field]
            applied += 1
        elif d and d >= 16 and d <= 40:
            skipped += 1

    return data, applied, skipped


def main():
    dry = "--dry-run" in sys.argv

    if not os.path.exists(DATA_FILE):
        print(f"❌ {DATA_FILE} 이 현재 디렉터리에 없습니다. insta-wizbee 프로젝트 루트에서 실행하세요.")
        sys.exit(1)

    data, applied, skipped = apply_pack()
    print(f"🧾 팩 적용 대상: {applied}개 Day (16~40 중)")
    if skipped:
        print(f"⚠️ Day 16~40 중 팩에 없는 Day: {skipped}")

    if dry:
        print("\n[DRY RUN] book_data.json 저장 생략.")
        # Preview a sample
        sample = next((p for p in data if p["day"] == 16), None)
        if sample:
            print("\n--- Preview (Day 16) ---")
            print(json.dumps(sample, ensure_ascii=False, indent=2))
        return

    tmp = DATA_FILE + ".tmp"
    with open(tmp, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    os.replace(tmp, DATA_FILE)
    print(f"\n💾 {DATA_FILE} 갱신 완료 ({applied}개 포스트 내용 교체).")
    print("👉 다음: `python insta_autopilot.py --seed` 로 RTDB 에도 반영하세요.")


if __name__ == "__main__":
    main()
